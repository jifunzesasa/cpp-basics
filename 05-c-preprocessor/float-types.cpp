// float-types.cpp
#include <iostream>
using namespace std;

int main( int argc, char ** argv ) {
	float f;
	double df;
	long double ldf;

	cout << "size of float f is " << sizeof(f) << endl;
	cout << "size of double float df is " << sizeof(df) << endl;
	cout << "size of long double float ldf is " << sizeof(ldf) << endl;

	return 0;
}
